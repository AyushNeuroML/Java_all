public class logicalopt {
    public static void main(String[] args) {
        System.out.println((3>2) && (5<0)); //logical and is when only both statements are true
        System.out.println((3<2) || (5>2)); //in logical or, only when both statements iare false , the result will be false
        System.out.println(!(3>2)); //logical not is used to reverse the result of the boolean
        
    }
    
}
